def inv_shift_rows_step(msg):
    temp = np.zeros(msg.shape, dtype=int)
    temp[:, 0, 0], temp[:, 0, 1], temp[:, 0, 2], temp[:, 0, 3] = msg[:, 0, 0], msg[:, 0, 1], msg[:, 0, 2], msg[:, 0, 3]
    temp[:, 1, 0], temp[:, 1, 1], temp[:, 1, 2], temp[:, 1, 3] = msg[:, 1, 3], msg[:, 1, 0], msg[:, 1, 1], msg[:, 1, 2]
    temp[:, 2, 0], temp[:, 2, 1], temp[:, 2, 2], temp[:, 2, 3] = msg[:, 2, 2], msg[:, 2, 3], msg[:, 2, 0], msg[:, 2, 1]
